    if(isfield(gltf.nodes{node_id},'skin'))
        skin_idx=gltf.nodes{node_id}.skin+1;
        for primitive_idx=1:numel(gltf.meshes{mesh_idx}.primitives)
            controller=documentNode.createElement("controller");
